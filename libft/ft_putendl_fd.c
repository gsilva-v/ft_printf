#include "libft.h"

int	ft_putendl_fd(char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		write(1, s + i, 1);
		i++;
	}
	write(1, "\n", 1);
	return (i + 1);
}
